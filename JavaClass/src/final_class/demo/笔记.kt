package final_class.demo

/*final关键字
    final是一个关键字，表示最终的，不可变的
    final修饰的类无法被继承
    final修饰的方法无法被覆盖
    final修饰的变量一旦赋值之后，不可重新赋值[不可二次赋值]
    final修饰的实例变量：必须在声明的时候赋值，因为实例变量声明时会有默认值
    final修饰的引用：final修饰的引用一旦指向某个对象之后，不能再指向其他对象，那么被指向的对象无法被垃圾回收器回收
                                final修饰的引用虽然指向某个对象之后不能指向其他对象，但是所指向的对象内部的内存是可以被修改的
    final修饰的实例变量是不可变的，这种变量一般和static联合使用，被称为“常量”








 */