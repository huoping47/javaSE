/*          泛型
泛型这种语法机制，只在编译阶段起作用，在运行阶段没什么用

泛型的好处是什么？
    第一，集合中存储的元素类型统一了
    第二，从集合中取出的元素类型是泛型的指定类型，不需要进行大量的”向下转型“；
    
    缺点是什么？
        导致集合中的存储元素缺乏多样性



























