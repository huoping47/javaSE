package package_import.demo

/*package 和import
    java语言当中的包机制
        1、包、又称为package，java中引用package这种机制是为了方便程序管理，不同功能的类被分门别类放到不同的软件包中，查找比较方便，管理比较方便，易维护
        2、package语法机制：
            在java源程序的第一行编写package语句
            package只能编写一个语句
            语法结构：
                package+包名
        3、包名的命名规范：
                公司域名倒序+项目名+模块名+功能名      例如：com.bjpowernode.oa.user.service;
                采用这种方式重名的几率较低，因为公司域名具有全球唯一性
                包名要求全部小写，包名也是标识符，必须遵循标识符的命名规则，一个包对应的是一个目录
        4、

import:
    import用来完成导入其他类，同一个包下的类不需要导入，不在同一个包下需要手动导入
    语法格式：
        import  类名;
        import  包名.*;
    什么时候需要import？
        不在同一个包下，或者不在java.unit.lang的包下









 */