package duotai_class

/*多态
	-语法机制：
	    Animal,Cat,Dog三个类之间的关系：
	        Cat继承Animal
	        Dog继承Animal
	        Cat和Dog之间没有任何继承关系
    -面向对象的三大特征：封装、继承、多态
    
    -关于多态的几个概念：
        *向上转型（upcasting）
            子类型-->父类型
            又被称为：自动类型装换
        *向下转型（downcasting）
            父类型-->子类型
            又被称为：强制类型转换【需要加强制类型转换符】
            
            *无论是向上转型还是向下转型，两者之间必须要有继承关系，没有继承关系，代码无法通过
            
多态的作用：
    降低程序的耦合低，提升程序的扩展力
    面向抽象编程，尽量不要面向具体编程
	
	
	
	
	
	
	
	
 */